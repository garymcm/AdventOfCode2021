// We want a cipher that maps signals to a common schema. We do this by
// deduction.
//
// Schema:
//
//   aaaa
//  b    c
//  b    c
//   dddd
//  e    f
//  e    f
//   gggg
//
function buildCipher(lineOfSignals) {
  // Get the different signals in `inputSignals`.
  function getDifference(compareTo, inputSignals) {
    let wrkArray
    if (Array.isArray(compareTo)) {
      wrkArray = compareTo
    } else {
      wrkArray = compareTo.split('')
    }
    for (let i = 0; i < inputSignals.length; i++) {
      let pos = wrkArray.indexOf(inputSignals[i])
      if (pos !== -1) {
        wrkArray.splice(pos, 1)
      }
    }
    return wrkArray
  }

  // `compareTo` is present in `inputSignals`.
  function fullyIncludes(compareTo, inputSignals) {
    let wrkArray = compareTo.split('')
    return wrkArray.every((c) => inputSignals.indexOf(c) !== -1)
  }

  // Length === 5 and fully includes one
  function getThree(one) {
    const lengthOf5 = lineOfSignals.filter((l) => l.length === 5)
    return lengthOf5.find((l) => fullyIncludes(one, l))
  }

  // Length === 5 and is different to `three` only by one signal and that one is not `b`
  function getFive() {
    const lengthOf5 = lineOfSignals.filter((l) => l.length === 5)
    return lengthOf5.find((l) => {
      const diff = getDifference(l, three)
      return diff.length === 1 && diff.includes(SignalCipher.b)
    })
  }

  // The only one that has signal different when compared to `one`.
  function getSix() {
    return lineOfSignals.find((l) => getDifference(one, l).length === 1)
  }

  // Remove `one` and `five` from `eight` should leave a length of 0. This is `nine`.
  function getNine() {
    return lineOfSignals.find((l) => {
      const step1 = getDifference(l, one)
      return getDifference(step1, five).length === 0
    })
  }

  // We'll remove signals as we decipher them which helps with the process of
  // elimination.
  function removeValue(value) {
    let idx = lineOfSignals.indexOf(value)
    if (idx !== -1) {
      lineOfSignals.splice(idx, 1)
    }
  }
  // We'll remove signals as we assign them which helps with the process of
  // elimination.
  function removeSignal(value) {
    let idx = signalsProgress.indexOf(value)
    if (idx !== -1) {
      signalsProgress.splice(idx, 1)
    }
  }

  const SignalCipher = {}
  const signalsProgress = ['a', 'b', 'c', 'd', 'e', 'f', 'g']

  // Rule one: find uniques.
  const one = lineOfSignals.find((l) => l.length === 2)
  removeValue(one)
  const four = lineOfSignals.find((l) => l.length === 4)
  removeValue(four)
  const seven = lineOfSignals.find((l) => l.length === 3)
  removeValue(seven)
  const eight = lineOfSignals.find((l) => l.length === 7)
  removeValue(eight)

  /*
   * Deduction phase
   */

  const three = getThree(one)
  removeValue(three)

  // Signal `b` is the difference between the results of comparing 8 and 3 and 8
  // and 4.
  let step1 = getDifference(eight, three)
  let step2 = getDifference(eight, four)
  SignalCipher.b = getDifference(step1, step2)[0]
  removeSignal(SignalCipher.b)
  removeValue(SignalCipher.b)

  // Five depends on SIGNALS.b
  const five = getFive()
  removeValue(five)

  // Two is the only length-5 value remaining.
  const two = lineOfSignals.find((l) => l.length === 5)
  removeValue(two)

  const six = getSix()
  removeValue(six)

  const nine = getNine()
  removeValue(nine)

  const zero = lineOfSignals[0]
  removeValue(zero)

  SignalCipher.a = getDifference(seven, one)[0]
  removeSignal(SignalCipher.a)
  SignalCipher.d = getDifference(eight, zero)[0]
  removeSignal(SignalCipher.d)
  SignalCipher.c = getDifference(one, six)[0]
  removeSignal(SignalCipher.c)
  SignalCipher.e = getDifference(eight, nine)[0]
  removeSignal(SignalCipher.e)
  // We know `c`, so `f` is the other one.
  SignalCipher.f = one.split('').find((ch) => ch !== SignalCipher.c)
  removeSignal(SignalCipher.f)
  SignalCipher.g = signalsProgress.pop()
  return SignalCipher
}
// For each digit we decipher it into the common schema, then covert it to a
// numeric value.
function createSignalValue(cipher, values) {
  let decipheredValues = []

  values.forEach((value) => {
    let signalValue = 0
    for (let i = 0; i < value.length; i++) {
      let normalSignal = findDigit(cipher, value[i])[0]
      if (!normalSignal) throw Error('Ouch')
      signalValue |= Math.pow(2, BINARY_IDX.indexOf(normalSignal))
    }
    decipheredValues.push(SCHEMA_VALUES.find((sv) => sv[0] === signalValue))
  })
  return decipheredValues.map((d) => d[1]).join('')
}

// Find the mapped digit.
function findDigit(cipher, d) {
  const cipherEntries = Object.entries(cipher)
  return cipherEntries.find(([key, value]) => {
    if (value === d) {
      return key
    }
  })
}

function computeSignalInteger(signals) {
  let result = 0
  for (let i = 0; i < signals.length; i++) {
    result |= Math.pow(2, BINARY_IDX.indexOf(signals[i]))
  }
  return result
}

const BINARY_IDX = 'abcdefg'

const SCHEMA = {
  0: 'abcefg',
  1: 'cf',
  2: 'acdeg',
  3: 'acdfg',
  4: 'bdcf',
  5: 'abdfg',
  6: 'abdefg',
  7: 'acf',
  8: 'abcdefg',
  9: 'abcdfg',
}

const SCHEMA_VALUES = Object.entries(SCHEMA).map(([k, v]) => {
  return [computeSignalInteger(v), k]
})

