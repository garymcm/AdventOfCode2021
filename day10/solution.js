const data = PUZZLE_DATA.split('\n').map((line) => line.trim())

const openers = Object.keys(SYNTAX)
const closers = Object.values(SYNTAX)

// Verify the line of text is balanced. Breaks early if it is not. Returns the
// number status of the line at the point of failure. In accordance with the
// spec, this doesn't consider an incomplete line to be a failure.
function verify(text) {
  const stack = []
  for (const c of text) {
    if (openers.includes(c)) {
      // push on the corresponding closer
      stack.unshift(SYNTAX[c])
    } else if (c === stack[0]) {
      // Pop off the bracket we're nested under
      stack.shift()
    } else if (closers.includes(c)) {
      // Unbalanced closer: push it on the stack to invoke the error condition.
      stack.unshift(`~${c}`)
      break
    }
  }
  return stack
}

// Get invalid chunks which contains an unbalanced closer.
function getInvalidChunks() {
  let invalidChucks = []
  for (let i = 0; i < data.length; i++) {
    const check = verify(data[i])
    if (check[0][0] === '~') {
      invalidChucks.push(check)
    }
  }
  return invalidChucks
}

// Get invalid chunks which contains an unbalanced opener.
function getIncompleteChunks() {
  let invalidChucks = []
  for (let i = 0; i < data.length; i++) {
    const check = verify(data[i])
    if (check?.[0]?.[0] !== '~') {
      invalidChucks.push(check)
    }
  }
  return invalidChucks
}

// Part 1 rules for computing the answer.
function getPartACharScore(ch) {
  switch (ch) {
    case ')':
      return 3
    case ']':
      return 57
    case '}':
      return 1197
    case '>':
      return 25137
    default:
      return 0
  }
}

// Part 2 rules for computing the answer.
function getPartBCharScore(ch) {
  switch (ch) {
    case ')':
      return 1
    case ']':
      return 2
    case '}':
      return 3
    case '>':
      return 4
    default:
      return 0
  }
}

// Loop over syntax result and compute the answer. The first entry on the stack
// is the unbalanced closer and will start with a '~'. Therefore, the second
// character is the basis of the calculation.
function computePart1(invalidChunks) {
  let total = 0
  for (let i = 0; i < invalidChunks.length; i++) {
    const ch = invalidChunks[i][0][1]
    total += getPartACharScore(ch)
  }
  return total
}

// Get the median value from the array of scores
function getMedianValue(arr) {
  arr.sort((a, b) => a - b)
  return arr[Math.floor(arr.length / 2)]
}

// Loop over the incomplete lines and score the characters that would be
// required to balance the line. Return the median of the scores.
function computePart2(incompleteChunks) {
  let lineScores = []
  for (let i = 0; i < incompleteChunks.length; i++) {
    let total = 0
    for (let j = 0; j < incompleteChunks[i].length; j++) {
      total *= 5
      const ch = incompleteChunks[i][j]
      total += getPartBCharScore(ch)
    }
    lineScores.push(total)
  }

  return getMedianValue(lineScores)
}

function solution() {
  // Part 1
  const invalidChunks = getInvalidChunks()
  const part1 = computePart1(invalidChunks)
  document.getElementById('answer_part_1').innerHTML = `${part1}`

  // Part 2
  const incompleteChunks = getIncompleteChunks()
  const part2 = computePart2(incompleteChunks)
  document.getElementById('answer_part_2').innerHTML = `${part2}`
}

window.onload = solution
